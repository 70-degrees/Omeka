<h3>About the Jewish Women&#8217;s Archive (<abbr title="Jewish Women&#8217;s Archive">JWA</abbr>)</h3>
<p>The mission of the Jewish Women&#8217;s Archive (<abbr title="Jewish Women&#8217;s Archive">JWA</abbr>) is to uncover, chronicle, and transmit the rich history of American Jewish women and their contributions to our families and our communities, to our people and our world. </p>
<p>Since its founding in 1995, the Jewish Women&#8217;s Archive (<abbr title="Jewish Women&#8217;s Archive">JWA</abbr>) has developed innovative formats and collaborative partnerships to transmit the rich history of American Jewish women and their accomplishments to a broad public. <abbr title="Jewish Women&#8217;s Archive">JWA</abbr> seeks to make known the contributions of outstanding Jewish women of achievement as well as the profound, but often unacknowledged impact Jewish women have had within their local communities. One of the first organizations in the Jewish community to recognize and invest in the potential of the Internet for academic, cultural, archival, and educational purposes, <abbr title="Jewish Women&#8217;s Archive">JWA</abbr> continues to innovate in its use of technology to convey and provide access to Jewish women&#8217;s stories on its website. </p>
<p>A national, non-profit membership organization headquartered in Brookline, MA, <abbr title="Jewish Women&#8217;s Archive">JWA</abbr> is entering its second decade of changing the way history is collected, recorded, and taught. </p>

