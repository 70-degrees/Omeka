<h3>Overview</h3>
<p>Katrina&#8217;s Jewish Voices is an online history project to collect, preserve, and present the American Jewish community&#8217;s experiences of Hurricane Katrina and their recollections of the Jewish communities of New Orleans and the Gulf Coast. The Jewish Women&#8217;s Archive organized the project, in collaboration with the Center for History and New Media, with generous funding from foundations and individual donors.</p>

<p>Katrina&#8217;s Jewish Voices welcomes members of the Jewish communities of New Orleans and the Gulf Coast, as well as Jews across the country, to tell their own stories of how the storm affected them. The project is collecting digital artifacts in a variety of forms, including photos, blog postings, podcasts, emails, essays and other first-hand accounts, from American Jews nationwide.</p>

<p>These collections will serve as a vital resource for future historians of the American Jewish experience, as well as for those interested in exploring how individuals and different faith communities responded to this vast humanitarian crisis.</p>

<p>Sharing our stories is also a way to create powerful connections in the present. We hope that those who contribute to this virtual archive and those who immerse themselves in the stories and images they find here come away moved and inspired by these varied Jewish voices of Katrina.</p>

