<h3>About the Katrina&#8217;s Jewish Voices Oral History Project</h3>

<p>In a complementary part of the Katrina&#8217;s Jewish Voices project, <abbr title="Jewish Women&#8217;s Archive">JWA</abbr> is partnering with the Goldring/Woldenberg Institute of Southern Jewish Life (<abbr title="Institute of Southern Jewish Life">ISJL</abbr>) to conduct 75-100 in-depth oral histories with members of the Jewish communities of New Orleans and the Gulf Coast. This will enable a wide cross-section of these historic Jewish communities to serve as &#8220;historic witnesses&#8221; to a watershed event in its (and our) communal history. These interviews will be permanently housed at ISJL and made available on the <abbr title="Jewish Women&#8217;s Archive">JWA</abbr> website. For more information about the Katrina&#8217;s Jewish Voices oral history project, please visit <a href="http://www.jwa.org">www.jwa.org</a>.</p>


<h3>About the Jewish Women&#8217;s Archive</h3>
<p>The mission of the Jewish Women&#8217;s Archive (<abbr title="Jewish Women&#8217;s Archive">JWA</abbr>) is to uncover, chronicle, and transmit the rich history of American Jewish women.</p>

<p>Since its founding in 1995, the Jewish Women&#8217;s Archive has been developing innovative formats and collaborative partnerships to transmit the rich history of American Jewish women and their accomplishments to a broad public. <abbr title="Jewish Women&#8217;s Archive">JWA</abbr> seeks to make known the contributions of outstanding Jewish women of achievement as well as the profound, but often unacknowledged impact Jewish women have had within their local communities. One of the first organizations in the Jewish community to recognize and invest in the potential of the internet for academic, cultural, archival, and educational purposes, <abbr title="Jewish Women&#8217;s Archive">JWA</abbr> continues to innovate in its use of technology to convey and provide access to Jewish women&#8217;s stories on its website.</p>

<p>A national, non-profit membership organization headquartered in <span class="locality">Brookline</span>, <abbr class="region" title="Massachusetts">MA</abbr>, <abbr title="Jewish Women&#8217;s Archive">JWA</abbr> is entering its second decade of changing the way history is researched, recorded, and taught.</p>

